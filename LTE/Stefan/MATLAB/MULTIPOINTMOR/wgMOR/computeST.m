% ***********************************************************************
% ***********************************************************************
%
% A Model Order Reduction Method for the Finite Elment Simulation 
% of inhomogenous Waveguides
%
% Studienarbeit von Alwin Schultschik, 2006
%
% ***********************************************************************
%
% Composition of frequency independent matrices
%
% ***********************************************************************
% ***********************************************************************
% ***********************************************************************

function [S_red, T_red] = computeST(k, S_0, S_1, S_2, S_3, S_4, T_0, T_1, T_2)

  S_red = S_0 + k * S_1 + k^2 * S_2 + k^3 * S_3 + k^4 * S_4;
  T_red = T_0 + k * T_1 + k^2 * T_2;


end
